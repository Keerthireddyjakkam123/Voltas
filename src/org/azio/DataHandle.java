package org.azio;

public class DataHandle {

	public static void main(String[] args) {

		String s = "Java Program";

		System.out.println(s);

		s = s.toLowerCase();

		System.out.println(s);

		s = s.toUpperCase();

		System.out.println(s);

		s.substring(2);

		System.out.println(s);

		char c = s.charAt(0);

		System.out.println(c);

	}

}
